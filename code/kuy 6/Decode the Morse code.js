decodeMorse = function (morseCode) {
    const morse = {
        '.-': 'a', '-...': 'b', '-.-.': 'c', '-..': 'd', '.': 'e',
        '..-.': 'f', '--.': 'g', '....': 'h', '..': 'i', '.---': 'j',
        '-.-': 'k', '.-..': 'l', '--': 'm', '-.': 'n', '---': 'o',
        '.--.': 'p', '--.-': 'q', '.-.': 'r', '...': 's', '-': 't',
        '..-': 'u', '...-': 'v', '.--': 'w', '-..-': 'x', '-.--': 'y',
        '--..': 'z', '.----': '1', '..---': '2', '...--': '3',
        '....-': '4', '.....': '5', '-....': '6', '--...': '7',
        '---..': '8', '----.': '9', '-----': '0', '.-.-.-': '.',
        '--..--': ',', '..--..': '?', '-..-.': '/', '.--.-.': '@'
    };

    return morseCode
        .split(" ")
        .map(v => morse[v] !== undefined ?morse[v] : 1)
        .join("")
        .replace(/11/g, " ")
        .replace(/1/g, " ")
        .trim();

    // Your code here
    // You can use MORSE_CODE[morse]
}

console.log(decodeMorse('.... . -.--   .--- ..- -.. .'))//'HEY JUDE'
console.log(decodeMorse('   .... . -.--   '))//'HEY
